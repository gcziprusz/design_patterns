// Ambiguous Viewpoint antipatterns is present is the model(s) do not express the viewpoint they represent. 

// The refactored example of this antipattern is a telephone exchange systems 
// design that significantly differs based on the perspective:

// - end user that cares about the ease of making calls, 
// - operator concerned about connecting phone numbers 
// - billing dept concerned with log of calls and formula for calculating charges

